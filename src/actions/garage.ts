/**
 * See note in src/actions/time-entries.ts: revalidatePath can throw
 * during the implicit re-render that follows a server action, which
 * surfaces as an opaque 500 on the POST. Wrap so the mutation result
 * still reaches the client even if the next render briefly hiccups.
 */
function safeRevalidate(path: string, type?: "layout" | "page") {
  try {
    if (type) revalidatePath(path, type);
    else revalidatePath(path);
  } catch (err) {
    // eslint-disable-next-line no-console
    console.error(`[garage actions] safeRevalidate(${path}) failed:`, err);
  }
}

