// ─────────────────────────────────────────────────────────────────────────────
// TODAY's REPAIRS
// ─────────────────────────────────────────────────────────────────────────────

export async function getGarageRepairsToday() {
  const ctx = await requireAnyAuth();

  const now = new Date();
  const startOfDay = new Date(now);
  startOfDay.setHours(0, 0, 0, 0);
  const endOfDay = new Date(now);
  endOfDay.setHours(23, 59, 59, 999);

  const jobs = await db
    .select({
      id: repairJobs.id,
      publicCode: repairJobs.publicCode,
      title: repairJobs.title,
      status: repairJobs.status,
      priority: repairJobs.priority,
      dueDate: repairJobs.dueDate,
      customerName: customers.name,
      unitRegistration: units.registration,
      unitBrand: units.brand,
      unitModel: units.model,
      unitStorageLocation: units.storageLocation,
      unitCurrentPosition: units.currentPosition,
      assignedUserName: users.name,
      finalCheckStatus: repairJobs.finalCheckStatus,
      jobType: repairJobs.jobType,
      garageAdminMessage: repairJobs.garageAdminMessage,
      garageAdminMessageAt: repairJobs.garageAdminMessageAt,
      garageAdminMessageReadAt: repairJobs.garageAdminMessageReadAt,
    })
    .from(repairJobs)
    .leftJoin(customers, eq(repairJobs.customerId, customers.id))
    .leftJoin(units, eq(repairJobs.unitId, units.id))
    .leftJoin(users, eq(repairJobs.assignedUserId, users.id))
    .where(
      and(
        isNull(repairJobs.deletedAt),
        isNull(repairJobs.archivedAt),
        or(
          // Today's repairs (any status)
          and(
            gte(repairJobs.dueDate, startOfDay),
            lte(repairJobs.dueDate, endOfDay)
          ),
          // Active/waiting repairs regardless of due date
          inArray(repairJobs.status, [
            "in_progress",
            "waiting_parts",
            "waiting_customer",
            "blocked",
            "ready_for_check",
          ])
        )
      )
    )
    .orderBy(asc(repairJobs.priority), asc(repairJobs.title));

  // Get task counts per job
  const jobIds = jobs.map((j) => j.id);
  if (jobIds.length === 0) return [];

  const taskCounts = await db
    .select({
      repairJobId: repairTasks.repairJobId,
      total: count(),
      done: sql<number>`count(*) filter (where ${repairTasks.status} = 'done')`,
      problem: sql<number>`count(*) filter (where ${repairTasks.status} = 'problem')`,
    })
    .from(repairTasks)
    .groupBy(repairTasks.repairJobId);

  const countsMap = new Map(
    taskCounts.map((c) => [
      c.repairJobId,
      { total: Number(c.total), done: Number(c.done), problem: Number(c.problem) },
    ])
  );

  // Pull the actionable "next" task per job so the Today screen can offer a
  // one-tap "mark this done" affordance directly on the card. We pick the first
  // in-progress task; otherwise the first pending one. Sort order falls back
  // to insertion order via createdAt to keep behaviour stable.
  const candidateTasks = await db
    .select({
      id: repairTasks.id,
      repairJobId: repairTasks.repairJobId,
      title: repairTasks.title,
      titleEs: repairTasks.titleEs,
      titleNl: repairTasks.titleNl,
      status: repairTasks.status,
      createdAt: repairTasks.createdAt,
    })
    .from(repairTasks)
    .where(
      and(
        inArray(repairTasks.repairJobId, jobIds),
        inArray(repairTasks.status, ["in_progress", "pending"]),
      ),
    )
    .orderBy(asc(repairTasks.createdAt));

  const nextTaskMap = new Map<
    string,
    {
      id: string;
      title: string;
      titleEs: string | null;
      titleNl: string | null;
      status: "pending" | "in_progress";
    }
  >();
  for (const t of candidateTasks) {
    const existing = nextTaskMap.get(t.repairJobId);
    if (!existing) {
      nextTaskMap.set(t.repairJobId, {
        id: t.id,
        title: t.title,
        titleEs: t.titleEs,
        titleNl: t.titleNl,
        status: t.status as "pending" | "in_progress",
      });
      continue;
    }
    // Prefer in-progress over pending if we encounter one later.
    if (existing.status !== "in_progress" && t.status === "in_progress") {
      nextTaskMap.set(t.repairJobId, {
        id: t.id,
        title: t.title,
        titleEs: t.titleEs,
        titleNl: t.titleNl,
        status: "in_progress",
      });
    }
  }

  // Get part request counts per job
  const partCounts = await db
    .select({
      repairJobId: partRequests.repairJobId,
      total: count(),
      received: sql<number>`count(*) filter (where ${partRequests.status} = 'received')`,
      pending: sql<number>`count(*) filter (where ${partRequests.status} in ('requested', 'ordered', 'shipped'))`,
    })
    .from(partRequests)
    .groupBy(partRequests.repairJobId);

  const partsMap = new Map(
    partCounts.map((c) => [
      c.repairJobId,
      { total: Number(c.total), received: Number(c.received), pending: Number(c.pending) },
    ])
  );

  // Pick the "next part to receive" per job for one-tap mark-received from
  // the Today card. Prefer a shipped part (closest to arrival), fall back to
  // ordered, then requested. Within a status, the earliest expected delivery
  // wins; if no ETA exists, the oldest request wins.
  const candidatePartRequests = await db
    .select({
      id: partRequests.id,
      repairJobId: partRequests.repairJobId,
      partName: partRequests.partName,
      status: partRequests.status,
      expectedDelivery: partRequests.expectedDelivery,
      createdAt: partRequests.createdAt,
    })
    .from(partRequests)
    .where(
      and(
        inArray(partRequests.repairJobId, jobIds),
        sql`${partRequests.status} in ('requested', 'ordered', 'shipped')`,
      ),
    );

  const STATUS_PRIORITY: Record<string, number> = { shipped: 0, ordered: 1, requested: 2 };
  type NextPartInternal = {
    id: string;
    name: string;
    status: "requested" | "ordered" | "shipped";
    expectedDelivery: Date | null;
    createdAt: number;
  };
  const nextPartMap = new Map<string, NextPartInternal>();
  for (const pr of candidatePartRequests) {
    if (!pr.repairJobId) continue;
    const candidate: NextPartInternal = {
      id: pr.id,
      name: pr.partName,
      status: pr.status as "requested" | "ordered" | "shipped",
      expectedDelivery: pr.expectedDelivery ? new Date(pr.expectedDelivery) : null,
      createdAt: pr.createdAt ? new Date(pr.createdAt).getTime() : Infinity,
    };
    const existing = nextPartMap.get(pr.repairJobId);
    if (!existing) {
      nextPartMap.set(pr.repairJobId, candidate);
      continue;
    }
    const a = STATUS_PRIORITY[candidate.status] ?? 9;
    const b = STATUS_PRIORITY[existing.status] ?? 9;
    if (a < b) {
      nextPartMap.set(pr.repairJobId, candidate);
      continue;
    }
    if (a === b) {
      const at = candidate.expectedDelivery?.getTime();
      const bt = existing.expectedDelivery?.getTime();
      if (at != null && bt != null) {
        if (at < bt) nextPartMap.set(pr.repairJobId, candidate);
      } else if (at != null && bt == null) {
        nextPartMap.set(pr.repairJobId, candidate);
      } else if (at == null && bt == null) {
        if (candidate.createdAt < existing.createdAt) nextPartMap.set(pr.repairJobId, candidate);
      }
    }
  }

  // Get workers per job
  const workers = await db
    .select({
      repairJobId: repairWorkers.repairJobId,
      userName: users.name,
    })
    .from(repairWorkers)
    .innerJoin(users, eq(repairWorkers.userId, users.id))
    .where(inArray(repairWorkers.repairJobId, jobIds));

  const workersMap = new Map<string, string[]>();
  for (const w of workers) {
    const list = workersMap.get(w.repairJobId) ?? [];
    list.push(w.userName);
    workersMap.set(w.repairJobId, list);
  }

  // Som van werkelijke (onafgeronde) minuten per klus. We tonen
  // tussentijds bewust de echte tijd — kwartaalafronding hoort pas bij
  // facturatie, niet tijdens het werk (dan lijkt het of een 9-minuten
  // sessie "15m" wordt en daarna weer terugspringt bij pauze, wat
  // werkers het gevoel geeft dat de timer "fout" telt).
  const timeTotals = await db
    .select({
      repairJobId: timeEntries.repairJobId,
      totalMinutes: sql<number>`coalesce(sum(${timeEntries.durationMinutes}), 0)`,
    })
    .from(timeEntries)
    .where(inArray(timeEntries.repairJobId, jobIds))
    .groupBy(timeEntries.repairJobId);

  const timeMap = new Map(timeTotals.map((t) => [t.repairJobId, Number(t.totalMinutes)]));

  return jobs.map((job) => {
    const np = nextPartMap.get(job.id);
    return {
      ...job,
      tasks: countsMap.get(job.id) ?? { total: 0, done: 0, problem: 0 },
      parts: partsMap.get(job.id) ?? { total: 0, received: 0, pending: 0 },
      workers: workersMap.get(job.id) ?? [],
      totalMinutes: timeMap.get(job.id) ?? 0,
      nextTask: nextTaskMap.get(job.id) ?? null,
      nextPart: np
        ? {
            id: np.id,
            name: np.name,
            status: np.status,
            expectedDelivery: np.expectedDelivery,
          }
        : null,
    };
  });
}

